"""
Transliteration Module

Provides transliteration functionality for Russian to English characters.
Based on original main.py implementation.
"""

def translit(char: str, source_lang: str = "ru", target_lang: str = "en") -> str:
    """
    Transliterate a single character from Russian to English.
    
    Args:
        char: Single character to transliterate
        source_lang: Source language (default: "ru")
        target_lang: Target language (default: "en")
    
    Returns:
        Transliterated character
    """
    # Russian to English transliteration mapping
    ru_to_en = {
        'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd', 'е': 'e', 'ё': 'yo',
        'ж': 'zh', 'з': 'z', 'и': 'i', 'й': 'y', 'к': 'k', 'л': 'l', 'м': 'm',
        'н': 'n', 'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't', 'у': 'u',
        'ф': 'f', 'х': 'h', 'ц': 'ts', 'ч': 'ch', 'ш': 'sh', 'щ': 'sch',
        'ъ': '', 'ы': 'y', 'ь': '', 'э': 'e', 'ю': 'yu', 'я': 'ya',
        
        'А': 'A', 'Б': 'B', 'В': 'V', 'Г': 'G', 'Д': 'D', 'Е': 'E', 'Ё': 'Yo',
        'Ж': 'Zh', 'З': 'Z', 'И': 'I', 'Й': 'Y', 'К': 'K', 'Л': 'L', 'М': 'M',
        'Н': 'N', 'О': 'O', 'П': 'P', 'Р': 'R', 'С': 'S', 'Т': 'T', 'У': 'U',
        'Ф': 'F', 'Х': 'H', 'Ц': 'Ts', 'Ч': 'Ch', 'Ш': 'Sh', 'Щ': 'Sch',
        'Ъ': '', 'Ы': 'Y', 'Ь': '', 'Э': 'E', 'Ю': 'Yu', 'Я': 'Ya'
    }
    
    # Handle both lowercase and uppercase
    if char in ru_to_en:
        return ru_to_en[char]
    elif char.upper() in ru_to_en:
        return ru_to_en[char.upper()]
    else:
        # Return original character if no transliteration found
        return char
